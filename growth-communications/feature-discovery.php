                  <div class="module">
                    <h1>点击目标</h1>
                    <p>特性探索提示把用户的注意力集中到特定的 UI 元素上。包括在圆形点击目标的外围，包裹一个较大的有不同背景色的区域。</p>
                    <p>提示只包含单个操作，不存在附加操作。</p>
                    <p>用户可以通过滑动手势、或点击提示背景之外的地方来关闭提示。</p>
                  </div>
