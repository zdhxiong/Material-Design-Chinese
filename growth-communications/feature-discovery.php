                      </tr>
                      <tr>
                        <td colspan="1" rowspan="1">
                          <p>限制 UI 中的特性探索提示的数量。例如，不要在一个会话中使用多个特性探索提示。</p>
                        </td>
                        <td colspan="1" rowspan="1">
                          <p>如果用户<strong>忽略</strong>了一条消息，在之后很长一段时间内不要再显示这条消息、或者和这条消息相关的消息。</p>
                          <p>如果用户<strong>接受</strong>了一条消息，说明用户觉得这条消息对他有用。可以在较短的时间内再次显示类似的消息。</p>
                        </td>
